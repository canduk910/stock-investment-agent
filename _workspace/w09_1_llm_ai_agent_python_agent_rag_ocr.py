def chunk_text(text, max_tokens=500):
    lines = text.split('\n')
    chunk = []
    tokens_count = 0
    chunks = []   # 최종 청크들을 담을 리스트
    in_table = False

    for line in lines:
        # 테이블 시작 감지
        if '|' in line:
            in_table = True

        if in_table:
            chunk.append(line)
            if not line.strip():  # 테이블 끝
                chunks.append("\n".join(chunk))
                chunk = []
                in_table = False
                tokens_count = 0
            continue

        line_tokens = len(line.split())
        if tokens_count + line_tokens > max_tokens and chunk:
            chunks.append("\n".join(chunk))
            chunk = []
            tokens_count = 0
        chunk.append(line)
        tokens_count += line_tokens

    if chunk:
        chunks.append("\n".join(chunk))

    return chunks

